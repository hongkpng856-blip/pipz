const ALL_CAT_ANIMS: Record<string, Grid[]> = {walk: CAT_WALK, idle: CAT_IDLE, play: CAT_PLAY}
const ALL_CAT_SPD: Record<string, number> = {walk: 150, idle: 180, play: 120}

function CatWalkAnim() {
  const ref = useRef<HTMLCanvasElement>(null)
  const [anim, setAnim] = useState<'walk'|'idle'|'play'>('walk')
  useEffect(() => {
    const c = ref.current!; const x = c.getContext('2d')!
    c.width = 32*6; c.height = 32*6; let f=0, t=0
    const frames = ALL_CAT_ANIMS[anim]
    const spd = ALL_CAT_SPD[anim]
    function r(ts:number){if(ts-t>=spd){t=ts;f=(f+1)%4}
      x.clearRect(0,0,c.width,c.height)
      for(let y=0;y<frames[f].length;y++)for(let X=0;X<frames[f][y].length;X++){const ci=parseInt(frames[f][y][X],10);x.fillStyle=C[ci];x.fillRect(X*6,y*6,6,6)}
      requestAnimationFrame(r)}
    const id=requestAnimationFrame(r);return()=>cancelAnimationFrame(id)
  },[anim])
  return <div>
    <canvas ref={ref} style={{imageRendering:'pixelated',borderRadius:8,width:140,height:140}}/>
    <div style={{display:'flex',gap:4,marginTop:8,justifyContent:'center'}}>
      {(['walk','idle','play'] as const).map(a=>
        <button key={a} onClick={()=>setAnim(a)}
          style={{flex:1,width:60,padding:'5px 0',borderRadius:8,border:'none',
            background:anim===a?ANIM_LABEL[a].c+'33':'#1a2338',
            color:anim===a?ANIM_LABEL[a].c:'#5a6d85',
            fontWeight:700,fontSize:11,cursor:'pointer',fontFamily:'inherit'}}>
          {ANIM_LABEL[a].i} {a}
        </button>
      )}
    </div>
  </div>
}

