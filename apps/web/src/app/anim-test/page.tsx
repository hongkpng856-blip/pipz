function draw(ctx: CanvasRenderingContext2D, frame: Grid, scale: number) {
  for (let y = 0; y < 20; y++) {
    const row = frame[y]
    for (let x = 0; x < 32; x++) {
      ctx.fillStyle = C[parseInt(row[x], 10)]
      ctx.fillRect(x * scale, y * scale, scale, scale)
    }
  }
}

